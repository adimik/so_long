#include "../libft.h"

int	ft_print_d(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		i += write(1, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		i += write(1, "-", 1);
		n = -n;
	}
	if (n < 10)
	{
		i += ft_print_c(n + '0');
	}
	else
	{
		i += ft_print_d(n / 10);
		i += ft_print_d(n % 10);
	}
	return (i);
}
